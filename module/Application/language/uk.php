* Повідомлення про нові особисті повідомлення. [Детальніше ...](#messages)
* Повідомлення про нові прийняті на сайт зображення. [Детальніше ...](#new)
* Повідомлення про нові завантажені (але ще не прийняті) зображення (тільки для зареєстрованих користувачів). [Детальніше ...](#inbox)

## Команди бота

### Старт: `/start`

Відправивши цю команду у відповідь ви отримаєте список підтримуваних команд.

### Особисті повідомлення: `/messages`

Відправте `/messages on`, щоб підписатися на отримання повідомлень.

Відправте `/messages off`, щоб відписатися.

### Нові зображення: `/new`

Відправте `/new BMW`, щоб підписатися на отримання нових фотографій BMW.

Відправте `/new BMW` повторно, щоб відписатися.

### Інбокс: `/inbox`

Потрібна попередня авторизація командою `/me`

Відправте `/inbox BMW`, щоб підписатися на отримання повідомлень про нові завантажені, але ще не прийняті фотографії BMW.

Відправте `/inbox BMW` повторно, щоб відписатися.

